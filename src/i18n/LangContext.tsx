const typewriterPhrases: Record<Lang, string[]> = {
  fr: [
    'votre trésorerie.',
    'vos économies.',
    'vos stablecoins.',
    'votre capital.',
    'vos liquidités.',
  ],
  en: [
    'your treasury.',
    'your savings.',
    'your stablecoins.',
    'your capital.',
    'your liquidity.',
  ],
}

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>('fr')
  const t = (key: string) => (translations[lang] as Record<string, string>)[key] || key
  const tArray = (key: string): string[] => {
    if (key === 'hero_typewriter') return typewriterPhrases[lang]
    return []
  }
  return <LangContext.Provider value={{ lang, setLang, t, tArray }}>{children}</LangContext.Provider>
}

export type { Lang }

export const useLang = () => useContext(LangContext)
